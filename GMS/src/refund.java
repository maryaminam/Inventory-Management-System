     int set_refundno(){
                  int no=0;
                try {
                String query = "SELECT TOP 1 refund_no FROM refund ORDER BY refund_no DESC ";
                PreparedStatement preparedStatement = conn.prepareStatement(query);
                ResultSet resultSet = preparedStatement.executeQuery();

                if(resultSet.next()){
                no = resultSet.getInt("refund_no")+1;
                }
                else{
                no=0;
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
                return no;
    }
     
