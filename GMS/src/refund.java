    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
          if("".equals(jTextField1.getText())||"".equals(jTextField4.getText())||"".equals(jTextField3.getText())||"".equals(jTextField2.getText())){
            JOptionPane.showMessageDialog(null, "ENTER COMPLETE INFORMATION");
            }
        else{
        int prod_id = Integer.parseInt(jTextField1.getText());     
        int quantity = Integer.parseInt(jTextField4.getText()); 
        int transno = Integer.parseInt(jTextField3.getText());
        int refund_id = Integer.parseInt(jTextField2.getText());
        try {
            String sql1 ="select rate,quantity from sales_child WHERE Transno ='"+transno+"' AND prod_id ='"+prod_id+"'";
            PreparedStatement ptst1=conn.prepareCall(sql1);
            ResultSet rs= ptst1.executeQuery();
                 // TODO add your handling code here:
                 String sql= "insert into refund values(?,?,?,?,?,?,?)";
                 PreparedStatement ptst=conn.prepareCall(sql);
                 java.sql.Date sqlDate = java.sql.Date.valueOf(LocalDate.now());
                 ptst.setInt(1,set_refundno());
                 ptst.setInt(2,prod_id);
                 ptst.setInt(3,quantity);
                 if(rs.next()){
                     if(rs.getInt("quantity")<quantity){
                      JOptionPane.showMessageDialog(null, "INVALID COMMAND");
                     }
                     else{
                       ptst.setDouble(4,rs.getDouble("rate"));
                     }
                 }
                 else{
                 JOptionPane.showMessageDialog(null, "PRODUCT NOT FOUND");
                 }
                 ptst.setDate(5,sqlDate);
                 ptst.setDouble(6,refund_id);
                 ptst.setDouble(7,transno);
                 ptst.executeUpdate();
                 JOptionPane.showMessageDialog(null, "REFUNDED");
             } catch (SQLException ex) {
                 JOptionPane.showMessageDialog(null, ex);
             }
        set_refundno();
        reset();
          }
    }//GEN-LAST:event_jButton1ActionPerformed

