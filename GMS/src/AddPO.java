    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        
         try {
        // Check if AppBy is a valid employee name
        String employeeCheckSql = "SELECT emp_id FROM Employee WHERE name = ?";
        try (PreparedStatement employeeCheckStmt = conn.prepareStatement(employeeCheckSql)) {
            employeeCheckStmt.setString(1, AppBy.getText());
            var resultSet = employeeCheckStmt.executeQuery();

            if (!resultSet.next()) {
                // Employee with the given name not found
                JOptionPane.showMessageDialog(this, "The name in Approved By is not a valid employee.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Valid employee name, get the emp_id
            String empId = resultSet.getString("emp_id");

            // Insert into PurchaseOrder
            String sql = "INSERT INTO PurchaseOrder VALUES(?,?,?,?,?,?,?,?,?,?)";
            PreparedStatement ptst = conn.prepareStatement(sql);

            ptst.setString(1, OrderID.getText());
            ptst.setString(2, Date.getText());
            ptst.setString(3, SupID.getText());
            ptst.setString(4, empId); // Using emp_id from the Employee table
            ptst.setString(5, Quantity.getText());
            ptst.setString(6, CostValue.getText());
            ptst.setString(7, SaleValue.getText());
            ptst.setString(8, (String) Status.getSelectedItem());
            ptst.setString(9, AppDate.getText());
            ptst.setString(10, AppBy.getText());

            ptst.executeUpdate();

            // Include prod_id when inserting into ProdOrder
            if (prod_id.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Please enter product ID.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            String prodOrderSql = "INSERT INTO ProdOrder VALUES (?, ?)";
            PreparedStatement ptstProdOrder = conn.prepareStatement(prodOrderSql);
            ptstProdOrder.setString(1, OrderID.getText());
            ptstProdOrder.setString(2, prod_id.getText());
            ptstProdOrder.executeUpdate();

            JOptionPane.showMessageDialog(null, "Data Added Successfully");
            reset();
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e);
    }
        
        
        
    }//GEN-LAST:event_saveActionPerformed

