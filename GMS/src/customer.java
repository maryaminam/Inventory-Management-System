    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        // TODO add your handling code here:
         try{
            Statement stmt = conn.createStatement();
            String phone = cust_phone.getText();
            String sql = "SELECT * FROM customer WHERE cust_phone = '"+phone+"'";
            ResultSet rs = stmt.executeQuery(sql);
            if(rs.next()){
                cust_id.setText(rs.getString("cust_id"));
                cust_name.setText(rs.getString("cust_name"));
                cust_type.setSelectedItem(rs.getString("cust_type"));
            }
            else{
                JOptionPane.showMessageDialog(null,"Record Not Found");
            }

        }catch (SQLException ex) {
                Logger.getLogger(customer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_searchActionPerformed

