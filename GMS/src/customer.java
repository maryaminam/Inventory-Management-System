    private boolean isCustomerIdExists(int cust_id) {

            try {
                pst = conn.prepareStatement("SELECT * FROM customer WHERE cust_id = ?");
                pst.setInt(1, cust_id);
                ResultSet rs = pst.executeQuery();
                return rs.next();  // Returns true if a record is found
            } catch (SQLException ex) {
                java.util.logging.Logger.getLogger(customer.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
                return false;
            }
    }
    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        // TODO add your handling code here:
        int id = Integer.parseInt(cust_id.getText());
        if (isCustomerIdExists(id)) {
            JOptionPane.showMessageDialog(this, "Customer with ID " + id + " already exists.", "Duplicate Entry", JOptionPane.ERROR_MESSAGE);
            
            cust_id.setText("");
            return;  // Exit the method to avoid further processing
        }
        try {
            pst = conn.prepareStatement("INSERT INTO customer(cust_id,cust_name,cust_phone,cust_type) VALUES (?,?,?,?)");
            pst.setInt(1, id);
            pst.setString(2,cust_name.getText());
            pst.setString(3,cust_phone.getText());
            pst.setString(4, cust_type.getSelectedItem().toString());
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null,"Data is sucessfully inserted.");
            reset();
            
            // Your existing code for resetting fields
            reset();
        } catch (SQLException ex) {
            java.util.logging.Logger.getLogger(customer.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Invalid Customer ID. Please enter a valid integer.", "Invalid Input", JOptionPane.ERROR_MESSAGE);
            cust_id.setText("");
        }
              
    }//GEN-LAST:event_addActionPerformed

